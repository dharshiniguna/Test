package org.Class_2;

public class Notes_Data_Types {
	
public static void main(String[] args) {
		
		// datatype refname = value;
		
		byte b = 127;
		System.out.println(b);
		
		short s = 32000;
		System.out.println(s);
		
		int i = 1234567890;
		System.out.println(i);
		
		long l = 8428775303l;
		System.out.println(l);
		
		float f = 20.11f;
		System.out.println(f);
		
		double d = 19.96;
		System.out.println(d);
		
		char c = 'G';
		System.out.print(c);
		
		String s1 = "_Kunguma Dharshini";
		System.out.print(s1);
	}
}
